'''
Write a simple script to demonstrate your understanding of dictionaries as data types 
'''
dict = {}
dict['one'] = "This is one"
dict[2] = "This is two"
mindict = {'name': 'john','code':6734, 'dept': 'sales'}
print (dict['one']) # Prints value for 'one' key
print (dict[2]) # Prints value for 2 key
print (mindict) # Prints complete dictionary
print (mindict.keys()) # Prints all the keys
print (mindict.values()) # Prints all the values