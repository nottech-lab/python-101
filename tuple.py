'''
Write a simple script to demonstrate your understanding of tuples as data types 
'''
tuple = ( 'frank', 786 , 2.23, 'samson', 70.2 )
mintuple = (123, 'samsontz')
print (tuple) # Prints complete list
print (tuple[0]) # Prints first element of the list
print (tuple[2:3]) # Prints elements starting from 2nd till 3rd
print (tuple[2:]) # Prints elements starting from 3rd element
print (mintuple * 2)# Prints list two times
print (tuple + mintuple) # Prints concatenated lists